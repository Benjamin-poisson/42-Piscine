int	ft_sqrt(int nb)
{
	int	mult;

	mult = 1;
	if (nb == 1)
		return (nb);
	while (mult <= nb / 2)
	{
		if (mult * mult == nb)
			return (mult);
		mult++;
	}
	return (0);
}
